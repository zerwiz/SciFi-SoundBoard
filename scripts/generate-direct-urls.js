#!/usr/bin/env node
/**
 * Generate config/direct-sound-urls.json from Freesound API (preview URLs).
 * Run once with FREESOUND_API_KEY, then commit the file so setup can install
 * real sounds without requiring users to have an API key.
 *
 * Usage: set FREESOUND_API_KEY=your_token then run: node scripts/generate-direct-urls.js
 */
const https = require('https');
const fs = require('fs');
const path = require('path');

const CREDENTIALS_PATH = path.join(__dirname, '..', 'config', 'freesound-credentials.json');

function getFreesoundApiKey() {
  const env = process.env.FREESOUND_API_KEY;
  if (env && env.trim()) return env.trim();
  if (fs.existsSync(CREDENTIALS_PATH)) {
    const cred = JSON.parse(fs.readFileSync(CREDENTIALS_PATH, 'utf8'));
    const key = cred.api_key || cred.token;
    if (key && key.trim()) return key.trim();
  }
  return null;
}

const key = getFreesoundApiKey();
if (!key) {
  console.error('Set FREESOUND_API_KEY or add config/freesound-credentials.json with api_key (get one at https://freesound.org/apiv2/apply).');
  process.exit(1);
}

const FREESOUND_IDS_PATH = path.join(__dirname, '..', 'config', 'freesound-ids.json');
const OUT_PATH = path.join(__dirname, '..', 'config', 'direct-sound-urls.json');

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { 'User-Agent': 'SciFi-SoundBoard-Generator/1.0' } }, (res) => {
      const chunks = [];
      res.on('data', (c) => chunks.push(c));
      res.on('end', () => resolve({ statusCode: res.statusCode, body: Buffer.concat(chunks) }));
    }).on('error', reject);
  });
}

async function main() {
  const ids = JSON.parse(fs.readFileSync(FREESOUND_IDS_PATH, 'utf8')).sounds;
  const urls = {};
  const apiBase = 'https://freesound.org/apiv2';
  for (const [appId, freesoundId] of Object.entries(ids)) {
    try {
      const { statusCode, body } = await fetch(
        `${apiBase}/sounds/${freesoundId}/?token=${encodeURIComponent(key.trim())}&fields=previews`
      );
      if (statusCode !== 200) throw new Error(`HTTP ${statusCode}`);
      const info = JSON.parse(body.toString());
      const url = info.previews && (info.previews['preview-hq-mp3'] || info.previews['preview-lq-mp3']);
      if (url) {
        urls[appId] = url;
        console.log(appId, '->', url.slice(0, 60) + '...');
      }
    } catch (e) {
      console.warn('Skip', appId, e.message || e);
    }
  }
  const out = {
    description: 'Direct preview URLs for setup without FREESOUND_API_KEY. Generated by scripts/generate-direct-urls.js',
    sounds: urls
  };
  fs.writeFileSync(OUT_PATH, JSON.stringify(out, null, 2), 'utf8');
  console.log('Wrote', OUT_PATH, 'with', Object.keys(urls).length, 'URLs.');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
